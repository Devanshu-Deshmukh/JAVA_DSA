package Arrays;

import java.util.Arrays;

public class Sort_Color {
    // Dutch National Algorithm
    public static void main(String[] args) {

        int[] nums = { 2, 0, 2, 1, 1, 0 };
        int low = 0;
        int mid = 0;
        int high = nums.length - 1;
        while (mid <= high) {
            if (nums[mid] == 0) {
                int temp = nums[low];
                nums[low] = nums[mid];
                nums[mid] = temp;
                low++;
                mid++;
            } else if (nums[mid] == 1) {
                mid++;

            } else {
                int temp = nums[mid];
                nums[mid] = nums[high];
                nums[high] = temp;
                high--;
            }

        }
        int[] result = nums;
        System.out.println("The Sorted array is: " + Arrays.toString(result));
    }

}
